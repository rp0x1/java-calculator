package terminal;

public class Ansi
{
    // DECLARE FOREGROUND COLORS
    public static final String RESET = "\u001b[0m";
    public static final String RED = "\u001b[31m";
    public static final String GREEN = "\u001b[32m";
    public static final String YELLOW = "\u001b[33m";
    public static final String BLUE = "\u001b[34m";
    public static final String PURPLE = "\u001b[35m";
    public static final String CYAN = "\u001b[36m";

    // DECLARE BACKGROUND COLORS 
    public static final String BG_WHITE = "\u001b[107;30m";
    public static final String BG_RED = "\u001b[101;37m";
    public static final String BG_GREEN = "\u001b[102;37m";
    public static final String BG_YELLOW = "\u001b[103;37m";
    public static final String BG_BLUE = "\u001b[104;37m";
    public static final String BG_PURPLE = "\u001b[105;37m";
    public static final String BG_CYAN = "\u001b[106;37m";

}